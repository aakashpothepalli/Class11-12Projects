    private void calcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcActionPerformed
        Double a, b, c, disc;
        Double[] root = new Double[2];

        a = Double.parseDouble(t1.getText());
        b = Double.parseDouble(t2.getText());
        c = Double.parseDouble(t3.getText());

        disc = (b * b) - (4 * a * c);

        root[0] = (-b + Math.sqrt(disc)) / (2 * a);
        root[1] = (-b - Math.sqrt(disc)) / (2 * a);

        if (disc < 0) 
            t4.setText("no real roots");

        else if (disc == 0) 
            t4.setText("equal roots and root is " + root[1]);
         else 
            t4.setText("different roots and roots are " + root[1] + " " + root[0]);
        


    }//GEN-LAST:event_calcActionPerformed

