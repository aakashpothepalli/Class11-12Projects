    private void jLabel4MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel4MouseReleased
      if(b==false )
      {    AnimationClass ab = new AnimationClass();
        ab.jLabelYUp(660, 20,1, 2, lb1);
        ab.jLabelYUp(590, 70,1, 2, contacts);
        ab.jLabelYUp(590, 70,1, 2, youtube);
        ab.jLabelYUp(590, 70,1, 2, phone);
        ab.jLabelYUp(590, 70,1, 2, calc);
        ab.jLabelXRight(-100, 190, 1, 2, feedback);
        ab.jLabelYDown(-120,60 ,1, 2, settings);
      b=true;
      }
      else
      {
          AnimationClass ab = new AnimationClass();
        ab.jLabelYDown(20, 660,1, 2, lb1);
        ab.jLabelYDown(70, 590,1, 2, contacts);
        ab.jLabelYDown(70, 590,1, 2, youtube);
        ab.jLabelYDown(70, 590,1, 2, phone);
        ab.jLabelYDown(70, 590,1, 2, calc);
        ab.jLabelXLeft(190, -100, 1, 2, feedback);
        ab.jLabelYUp(60,-120 ,1, 2, settings);
        b= false;
      }
    }//GEN-LAST:event_jLabel4MouseReleased

